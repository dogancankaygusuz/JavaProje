public class Main {
    public static void main(String[] args) {

        System.out.println(Math.abs(-100));          // mutlak değer
        System.out.println(Math.cbrt(27));           // küp kök
        System.out.println(Math.ceil(5.6765));       // en yakın büyük tam sayıya yuvarlar
        System.out.println(Math.floor(2.8462));      // en yakın küçük tam sayıya yuvarlar
        System.out.println(Math.log10(1000));        // 10 tabanında log
        System.out.println(Math.max(21, 23));        // 2 sayıdan maxı verir (minde de var)
        System.out.println(Math.pow(3,4));           // üs alma
        System.out.println(Math.random());           // 0 ile 1 arası rastgele deger
        System.out.println(Math.random() * 10);      // 0 ile 10 arası rastgele deger
        System.out.println(Math.round(23.4));        // en yakın tam sayıya yuvarlar
        System.out.println(Math.signum(-343));       // sayının işaretini verir
        System.out.println(Math.sqrt(196));          // karekök
    }
}